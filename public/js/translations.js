// ===== LANGUAGE SYSTEM =====
let currentLang = localStorage.getItem('lazzat-lang') || 'en';

function t(key) {
  return translations[currentLang][key] || translations['en'][key] || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-t]').forEach(el => {
    const key = el.getAttribute('data-t');
    const attr = el.getAttribute('data-t-attr');
    if (attr) {
      el.setAttribute(attr, t(key));
    } else {
      el.textContent = t(key);
    }
  });
  document.querySelectorAll('[data-t-html]').forEach(el => {
    el.innerHTML = t(el.getAttribute('data-t-html'));
  });
  if (currentLang === 'ur') {
    document.body.classList.add('urdu-mode');
    document.documentElement.setAttribute('dir', 'rtl');
  } else {
    document.body.classList.remove('urdu-mode');
    document.documentElement.setAttribute('dir', 'ltr');
  }
}

function toggleLang() {
  currentLang = currentLang === 'en' ? 'ur' : 'en';
  localStorage.setItem('lazzat-lang', currentLang);
  applyTranslations();
  // Re-render dynamic content
  if (typeof renderRecipes === 'function') renderRecipes(currentResults || []);
  if (typeof renderPlanner === 'function') renderPlanner();
  if (typeof renderGrocery === 'function') renderGrocery();
}

// ===== TOAST =====
function showToast(msg, type = 'info') {
  const container = document.getElementById('toast-container') || (() => {
    const c = document.createElement('div');
    c.id = 'toast-container';
    c.className = 'toast-container';
    document.body.appendChild(c);
    return c;
  })();
  const toast = document.createElement('div');
  toast.className = `toast ${type}`;
  toast.innerHTML = `<span>${type === 'success' ? '✅' : type === 'error' ? '❌' : 'ℹ️'}</span> ${msg}`;
  container.appendChild(toast);
  requestAnimationFrame(() => toast.classList.add('show'));
  setTimeout(() => {
    toast.classList.remove('show');
    setTimeout(() => toast.remove(), 400);
  }, 3000);
}

// ===== NAVBAR SCROLL =====
function initNavbar() {
  const nav = document.querySelector('.navbar');
  if (!nav) return;
  window.addEventListener('scroll', () => {
    nav.classList.toggle('scrolled', window.scrollY > 30);
  });
  // Hamburger
  const hamburger = document.querySelector('.hamburger');
  const mobileMenu = document.querySelector('.mobile-menu');
  if (hamburger && mobileMenu) {
    hamburger.addEventListener('click', () => mobileMenu.classList.toggle('open'));
  }
  // Active link
  const path = window.location.pathname.split('/').pop() || 'index.html';
  document.querySelectorAll('.nav-links a, .mobile-menu a').forEach(a => {
    if (a.getAttribute('href') === path || a.getAttribute('href') === '../' + path) {
      a.classList.add('active');
    }
  });
}

