// Cache Spoonacular detail objects so recipe-detail page can load them
const _spoonaCache = {};

function getSpoonaFromCache(id) {
  return _spoonaCache[id] || null;
}

// Convert Spoonacular recipe → MealDB-compatible shape
function normalizeSpoonacular(r) {
  const id = 'sp-' + r.id;

  // Build ingredient fields like MealDB strIngredient1..20
  const ingredientFields = {};
  const ingredients = r.extendedIngredients || [];
  ingredients.slice(0, 20).forEach((ing, i) => {
    const n = i + 1;
    ingredientFields[`strIngredient${n}`] = ing.name || '';
    ingredientFields[`strMeasure${n}`]    = ing.original || '';
  });
  for (let i = ingredients.length + 1; i <= 20; i++) {
    ingredientFields[`strIngredient${i}`] = '';
    ingredientFields[`strMeasure${i}`]    = '';
  }

  // Build step-by-step instructions
  let instructions = '';
  if (r.analyzedInstructions && r.analyzedInstructions[0]) {
    instructions = r.analyzedInstructions[0].steps
      .map(s => s.step)
      .join('\n');
  } else if (r.instructions) {
    instructions = r.instructions;
  }

  const normalized = {
    idMeal        : id,
    strMeal       : r.title || 'Unknown',
    strCategory   : (r.dishTypes && r.dishTypes[0]) || 'Pakistani',
    strArea       : 'Pakistani',
    strInstructions: instructions,
    strMealThumb  : r.image || 'https://www.themealdb.com/images/media/meals/default.jpg',
    strYoutube    : r.sourceUrl || '',
    strSource     : r.sourceUrl || '',
    readyInMinutes: r.readyInMinutes || null,
    servings      : r.servings || null,
    _source       : 'spoonacular',
    ...ingredientFields,
  };

  _spoonaCache[id] = normalized;
  return normalized;
}

// Search Spoonacular for a single keyword, returns normalized meals
async function searchSpoonacular(query, number = 20) {
  if (!SPOONACULAR_KEY) return [];
  try {
    const url = `https://api.spoonacular.com/recipes/complexSearch`
      + `?apiKey=${SPOONACULAR_KEY}`
      + `&query=${encodeURIComponent(query)}`
      + `&cuisine=pakistani,indian`
      + `&number=${number}`
      + `&addRecipeInformation=true`
      + `&fillIngredients=true`
      + `&instructionsRequired=true`;

    const res  = await fetch(url);
    const data = await res.json();

    if (data.status === 'failure' || !data.results) return [];
    return data.results.map(normalizeSpoonacular);
  } catch (e) {
    console.warn('Spoonacular search failed:', e);
    return [];
  }
}

// Fetch full detail for a Spoonacular recipe by numeric ID
async function fetchSpoonaDetail(numericId) {
  if (!SPOONACULAR_KEY) return null;
  try {
    const url = `https://api.spoonacular.com/recipes/${numericId}/information`
      + `?apiKey=${SPOONACULAR_KEY}&includeNutrition=false`;
    const res  = await fetch(url);
    const data = await res.json();
    return normalizeSpoonacular(data);
  } catch (e) {
    console.warn('Spoonacular detail failed:', e);
    return null;
  }
}

// Bulk fetch Pakistani recipes from Spoonacular using keyword list
async function fetchSpoonakulaPakistani() {
  if (!SPOONACULAR_KEY) return [];
  const results = [];
  // Run 3 keywords at a time to avoid hammering the free tier
  for (let i = 0; i < PAKISTANI_KEYWORDS.length; i += 3) {
    const batch = PAKISTANI_KEYWORDS.slice(i, i + 3);
    const settled = await Promise.allSettled(
      batch.map(kw => searchSpoonacular(kw, 10))
    );
    settled.forEach(r => {
      if (r.status === 'fulfilled') results.push(...r.value);
    });
  }
  return results;
}

