// Fetch detail for any recipe ID (MealDB or Spoonacular)
async function fetchMealDetail(id) {
  const sid = String(id);

  // Already cached from the listing page
  if (sid.startsWith('sp-')) {
    const cached = getSpoonaFromCache(sid);
    if (cached) return cached;
    // Re-fetch if not cached (direct URL visit)
    const numId = sid.replace('sp-', '');
    return await fetchSpoonaDetail(numId);
  }

  return await fetchMealDBDetail(sid);
}

// Search across both APIs
async function searchMeals(query) {
  const [mdbRes, spRes] = await Promise.allSettled([
    fetch(`${MEALDB_API}/search.php?s=${encodeURIComponent(query)}`)
      .then(r => r.json()).then(d => d.meals || []),
    searchSpoonacular(query, 20)
  ]);
  const mdb = mdbRes.status === 'fulfilled' ? mdbRes.value : [];
  const sp  = spRes.status  === 'fulfilled' ? spRes.value  : [];
  return deduplicateRecipes([...sp, ...mdb]);
}

// Search by ingredient (MealDB only — Spoonacular needs paid plan for this)
async function searchByIngredient(ingredient) {
  const res  = await fetch(`${MEALDB_API}/filter.php?i=${encodeURIComponent(ingredient.trim())}`);
  const data = await res.json();
  return data.meals || [];
}

function deduplicateRecipes(meals) {
  const seen = new Set();
  return meals.filter(m => {
    const key = m.idMeal || m.strMeal;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

// Main loader — called on the Recipes page
async function fetchPakistaniRecipes() {
  const [mdbResult, spResult] = await Promise.allSettled([
    // MealDB: Pakistani + Indian + Moroccan areas
    Promise.all([
      fetchByArea('Pakistani'),
      fetchByArea('Indian'),
      fetchByArea('Moroccan'),
    ]).then(([pak, ind, mor]) => [...pak, ...ind, ...mor]),

    // Spoonacular: keyword-based Pakistani recipes
    fetchSpoonakulaPakistani()
  ]);

  const mdb = mdbResult.status === 'fulfilled' ? mdbResult.value : [];
  const sp  = spResult.status  === 'fulfilled' ? spResult.value  : [];

  // Spoonacular results go first (more and better)
  return deduplicateRecipes([...sp, ...mdb]);
}


